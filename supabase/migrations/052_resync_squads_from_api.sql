update public.top_scorer_picks t
set player_id = canon.id
from (values
  ('Portugal','C. Ronaldo', 874),
  ('Brazil','M. Cunha', 1165),
  ('Brazil','I. Thiago', 196156)
) as r(team, orphan_name, target_api)
join public.players orphan on orphan.team = r.team and orphan.full_name = r.orphan_name and orphan.api_player_id is null
join public.players canon  on canon.api_player_id = r.target_api
where t.player_id = orphan.id;

-- 2) PRUNE primeiro: remove tudo que não está na API e não tem palpite.
--    Tem que vir ANTES do rename (2c->iniciais da API) p/ não colidir com linhas
--    duplicadas obsoletas (ex: "R. Doan" antigo sem api_id vs o oficial da API).
delete from public.players p
where not exists (select 1 from _api a where a.api_id = p.api_player_id)
  and not exists (select 1 from public.top_scorer_picks t where t.player_id = p.id);

-- 3) atualiza as linhas sobreviventes (linkadas por api_player_id) c/ nome/numero/posicao/time da API
update public.players p
set full_name = a.name, shirt_number = a.number, position = a.position, team = a.team
from _api a where p.api_player_id = a.api_id;

-- 4) insere os jogadores da API que ainda não existem
insert into public.players (full_name, team, position, shirt_number, api_player_id)
select a.name, a.team, a.position, a.number, a.api_id
from _api a
where not exists (select 1 from public.players p where p.api_player_id = a.api_id)
on conflict (full_name, team) do nothing;

-- 5) GUARDS (qualquer falha => ROLLBACK de tudo)
do $$
declare before int; aft int; bad int; missing int; us int; badteams text;
begin
  select c into before from _pc_before;
  select count(*) into aft from public.top_scorer_picks;
  if before <> aft then raise exception 'palpites mudaram de quantidade: % -> %', before, aft; end if;

  select count(*) into bad from public.top_scorer_picks t
    join public.players p on p.id = t.player_id
    where not exists (select 1 from _api a where a.api_id = p.api_player_id);
  if bad > 0 then raise exception '% palpite(s) apontando p/ jogador fora da API', bad; end if;

  select count(*) into missing from _api a
    where not exists (select 1 from public.players p where p.api_player_id = a.api_id);
  if missing > 0 then raise exception '% jogador(es) da API não entraram (conflito de nome?)', missing; end if;

  select count(*) into us from public.players where team = 'United States';
  if us > 0 then raise exception 'sobraram % linha(s) do time fantasma United States', us; end if;

  select string_agg(team || '=' || c, ', ') into badteams from (
    select team, count(*) c from public.players group by team having count(*) < 23 or count(*) > 28
  ) z;
  if badteams is not null then raise exception 'time(s) fora da faixa 23-28: %', badteams; end if;
end $$;

